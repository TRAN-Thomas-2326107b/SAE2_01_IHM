package iut.jeu_echec.Jeu.Pieces;

/**
 * Enumération des différents types de pièces dans un jeu d'échecs.
 */
public enum Pieces {
    /**
     * Représente une case vide.
     */
    RIEN,

    /**
     * Représente un pion.
     */
    PION,

    /**
     * Représente un cavalier.
     */
    CAVALIER,

    /**
     * Représente une tour.
     */
    TOUR,

    /**
     * Représente un fou.
     */
    FOU,

    /**
     * Représente une dame.
     */
    DAME,

    /**
     * Représente un roi.
     */
    ROI
}




